class Solution {
    public int countBeautifulPairs(int[] nums) {
        int res = 0;
        int n = nums.length;
        for (int i = 0; i < n; i++) {
            int d = 0;
            for (int t = nums[i]; t > 0; t /= 10) {
                d = t % 10;
            }
            for (int j = i + 1; j < n; j++) {
                if (gcd(d, nums[j] % 10) == 1) {
                    res++;
                }
            }
        }
        return res;
    }

    int gcd(int a, int b) {
        while (b != 0) {
            int t = a % b;
            a = b;
            b = t;
        }
        return a;
    }
}
