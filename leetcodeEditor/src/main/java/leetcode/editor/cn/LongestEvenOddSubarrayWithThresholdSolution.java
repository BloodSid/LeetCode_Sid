class Solution {
    public int longestAlternatingSubarray(int[] nums, int threshold) {
        int n = nums.length;
        int res = 0;
        int[] dp = new int[n + 1];
        for (int i = 0; i < n; i++) {
            if (nums[i] > threshold) {
                continue;
            } else if (dp[i] > 0 && i > 0 && nums[i] % 2 != nums[i - 1] % 2) {
                dp[i + 1] = Math.max(dp[i + 1], dp[i] + 1);
            } else if (nums[i] % 2 == 0) {
                dp[i + 1] = Math.max(dp[i + 1], 1);
            }
            res = Math.max(res, dp[i + 1]);
        }
        return res;
    }
}
