class Solution {
    public int climbStairs(int n) {
        int[] dp = new int[n + 2];
        for (int i = 0; i < dp.length; i++) {
            if (i == 0 || i == 1) {
                dp[i] = i;
            } else {
                dp[i] = dp[i - 1] + dp[i - 2];
            }
        }
        return dp[n + 1];
    }
}
