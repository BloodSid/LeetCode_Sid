class Solution {
    public int scoreOfParentheses(String s) {
        Deque<Integer> stack = new ArrayDeque<>();
        stack.push(0);
        for (char c : s.toCharArray()) {
            if (c == '(') stack.push(0);
            else {
                Integer pop = stack.pop();
                int val = pop == 0 ? 1 : 2 * pop;
                stack.push(stack.pop() + val);
            }
        }
        return stack.pop();
    }
}
