class Solution {
    public List<Integer> twoOutOfThree(int[] nums1, int[] nums2, int[] nums3) {
        int[] f1 = new int[101];
        int[] f2 = new int[101];
        int[] f3 = new int[101];
        count(nums1, f1);
        count(nums2, f2);
        count(nums3, f3);
        List<Integer> res = new ArrayList<>();
        for (int i = 0; i < f1.length; i++) {
            if (f1[i] + f2[i] + f3[i] >= 2) res.add(i);
        }
        return res;
    }

    void count(int[] nums, int[] f) {
        for (int num : nums) {
            f[num] = 1;
        }
    }
}
