class Solution {
    public boolean carPooling(int[][] trips, int capacity) {
        int[] d = new int[1002];
        for (int[] trip : trips) {
            int from = trip[1], to = trip[2];
            d[from] += trip[0];
            d[to] -= trip[0];
        }
        int num = 0;
        for (int i : d) {
            num += i;
            if (num > capacity) return false;
        }
        return true;
    }
}
